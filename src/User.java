public class User {
    private String nomne;
    private int matricula;

    public String getNomne() {
        return nomne;
    }

    public void setNomne(String nomne) {
        this.nomne = nomne;
    }

    public int getMatricula() {
        return matricula;
    }

    public void setMatricula(int matricula) {
        this.matricula = matricula;
    }
}
